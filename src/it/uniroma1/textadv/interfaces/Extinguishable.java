package it.uniroma1.textadv.interfaces;

/**
 * <code>Interfaccia funzionale</code> che rappresenta tutti gli oggetti che possono essere estinti, come un camino acceso o un falò.
 * @author 1849661
 *
 */
public interface Extinguishable {

	/**
	 * Metodo astratto che permette di estinguere l'oggetto.
	 */
	abstract void extinguish();
}