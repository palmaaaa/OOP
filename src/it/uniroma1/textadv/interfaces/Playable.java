package it.uniroma1.textadv.interfaces;

/**
 * <code>Interfaccia funzionale</code> che rappresenta un oggetto con il quale è possibile giocare.
 * @author 1849661
 *
 */
public interface Playable {

	/**
	 * Metodo astratto che permette al giocatore di giocare con l'oggetto.
	 */
	abstract void play();
}